package main

import "fmt"

func main() {
	deposit1 := 100000.0
	for month := 1; month <= 12; month++ {
		fmt.Printf("Месяц %d:\n", month)
		CalculateDeposit(&deposit1)
		fmt.Println()
	}

	deposit2 := 100000.0
	for month := 1; month <= 12; month++ {
		fmt.Printf("Месяц %d:\n", month)
		InterestCalculation(&deposit2)
		fmt.Println()
	}

}

//Начальный вклад равен 100000 рублей. Реализуйте функции для ежемесячного начисления процентов по ставке 5% годовых. Капитализируйте проценты
//ежемесячно и выводите сумму вклада после каждого начисления. Если сумма вклада превышает 150000 рублей, выведите сообщение "Достигнут лимит вклада"

func CalculateDeposit(deposit *float64) {
	monthlyRate := 0.05 / 12 // 5% годовых
	interest := *deposit * monthlyRate
	*deposit += interest
	fmt.Printf("Сумма вклада после начисления процентов: %.2f рублей\n", *deposit)

	if *deposit > 150000 {
		fmt.Println("Достигнут лимит вклада")
	}

}

