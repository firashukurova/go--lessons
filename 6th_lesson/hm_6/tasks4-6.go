//Выводите баланс после каждого начисления. Если баланс становится больше 300000 рублей, выведите сообщение "Достигнут лимит начислений".

func InterestCalculation(deposit *float64) {
	percent := *deposit * 0.05 //5% от текущей суммы
	*deposit += percent
	fmt.Printf("Новый баланс: %.2f рублей\n", *deposit)

	if *deposit > 300000 {
		fmt.Println("Достигнут лимит начислений")
	}

}
